import numpy as np
import matplotlib.pyplot as plt
from scipy.optimize import minimize
from scipy.stats import chisquare, chi2
from numpy import linalg # <-- ADDED THIS
import time

# --- Constants from the problem set ---
N_EVENTS = 10000       # Number of events per experiment
N_PVAL_LOOPS = 1000    # Number of experiments for p-value (Task 1)
N_FIT_LOOPS = 500      # Number of experiments for fitting (Task 4/5)
N_BINS = 50            # Number of bins for chi-square
X_MIN = -1.0           # x-domain min
X_MAX = 1.0            # x-domain max

# --- PDF and CDF Definitions ---

def f0(x):
    """
    The "true" PDF (nature), f_0(x).
    """
    return (3/8) * (1 + x**2)

def F0(x):
    """
    The analytical integral (antiderivative) of f0(x),
    used for calculating expected counts.
    """
    return (3/8) * (x + (x**3 / 3))

def f_fit(x, A):
    """
    The model PDF we are fitting, f(x; A).
    """
    return (3/8) * (1 + x**2 + A * x)

def F_fit(x, A):
    """
    The analytical integral (antiderivative) of f_fit(x, A),
    used for calculating expected counts from the fit.
    """
    return (3/8) * (x + (x**3 / 3) + A * (x**2 / 2))

# --- Event Generator ---

def generate_f0(N):
    """
    Generate N events according to f0(x) using the
    Acceptance-Rejection (Hit-or-Miss) method.
    """
    # Max value of f0(x) on [-1, 1] is at x = +/- 1
    f_max = f0(X_MAX) # f_max = 3/4

    accepted_events = []
    while len(accepted_events) < N:
        # Generate N_try events. Efficiency is ~2/3, so we add a buffer.
        N_try = int((N - len(accepted_events)) / 0.66) + 1

        # 1. Generate trial x values uniformly in the domain
        x_trial = np.random.uniform(X_MIN, X_MAX, N_try)

        # 2. Generate trial y values uniformly in [0, f_max]
        y_trial = np.random.uniform(0, f_max, N_try)

        # 3. Calculate the PDF value at the trial x
        pdf_val = f0(x_trial)

        # 4. Accept events where y_trial <= PDF(x_trial)
        accepted = x_trial[y_trial <= pdf_val]
        accepted_events.extend(accepted)

    # Return exactly N events
    return np.array(accepted_events[:N])

# --- Helper Functions ---

def get_expected_counts(N_total, bins, CDF_func, *args):
    """
    Calculates the expected counts in each bin for a given CDF.
    """
    # Calculate CDF at bin edges
    cdf_edges = CDF_func(bins, *args)
    # Expected probability in each bin is the difference in CDF
    bin_probabilities = np.diff(cdf_edges)
    # Expected counts
    return N_total * bin_probabilities

def plot_histogram(data, title, xlabel, bins=50):
    """Utility function to plot a histogram."""
    plt.figure(figsize=(10, 6))
    plt.hist(data, bins=bins, density=True, edgecolor='black', alpha=0.7)
    plt.title(title)
    plt.xlabel(xlabel)
    plt.ylabel('Density')
    plt.grid(True, linestyle='--', alpha=0.6)
    plt.show()

